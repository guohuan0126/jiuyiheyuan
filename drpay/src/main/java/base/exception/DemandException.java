package base.exception;

import base.error.ErrorCode;

public class DemandException extends ErrorCodeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4380581015043494331L;

	public DemandException() {
		super();
	}

	public DemandException(ErrorCode code, String message) {
		super(code, message);
	}

	public DemandException(ErrorCode code) {
		super(code);
	}

	public DemandException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DemandException(String message, Throwable cause) {
		super(message, cause);
	}

	public DemandException(String message) {
		super(message);
	}

	public DemandException(Throwable cause) {
		super(cause);
	}
	
}
