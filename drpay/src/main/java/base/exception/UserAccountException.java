package base.exception;

import base.error.ErrorCode;

/**
 * 用户账户异常
 */
public class UserAccountException extends AccountException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2819923194715001529L;

	public UserAccountException() {
	}

	public UserAccountException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UserAccountException(String message, Throwable cause) {
		super(message, cause);
	}

	public UserAccountException(String message) {
		super(message);
	}

	public UserAccountException(Throwable cause) {
		super(cause);
	}

	public UserAccountException(ErrorCode code) {
		super(code);
	}

	public UserAccountException(ErrorCode code, String message) {
		super(code, message);
	}
}
