package com.duanrong.drpay.jsonservice.param;

public class RepayParameter  extends Parameter{

	private static final long serialVersionUID = 6403754797411771450L;

	private String repayId;
	
	private boolean isBeforeRepay;

	public String getRepayId() {
		return repayId;
	}

	public void setRepayId(String repayId) {
		this.repayId = repayId;
	}

	public boolean isBeforeRepay() {
		return isBeforeRepay;
	}

	public void setBeforeRepay(boolean isBeforeRepay) {
		this.isBeforeRepay = isBeforeRepay;
	}
	
	
}
