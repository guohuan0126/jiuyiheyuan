package com.duanrong.drpay.business.payment.model;

/**
 * 银行和渠道汇总信息
 * 
 */
public class PaymentBankChannel {

	private Integer id;      // 编号
	private Integer bankId;    // 银行信息表Id
	private Integer channelId;   // 支付渠道表ID
	private Double oneMoney;    // 单笔限额
	private Double dayMoney;    // 单日限额
	private Double monthMoney;    // 单月限额
	private Integer onlineBanking;   // 是否支持网银 1支持 2不支持
	private Integer quickRecharge;    // 是否支持快捷 1支持 2不支持
	private Integer tiecard;    // 是否支持绑卡 1支持 2不支持
	private Integer urgentWithdrawals;   // 是否支持加急提现 1支持 2不支持
	private Integer sort;   // 排序
	private Integer isDel;   // 是否删除，1显示 2显示
	private PaymentBankInfo bankInfo;  //银行信息
	private PaymentChannel channel;   //渠道信息
	

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getBankId() {
		return bankId;
	}

	public void setBankId(Integer bankId) {
		this.bankId = bankId;
	}

	public Integer getChannelId() {
		return channelId;
	}

	public void setChannelId(Integer channelId) {
		this.channelId = channelId;
	}

	public Double getOneMoney() {
		return oneMoney;
	}

	public void setOneMoney(Double oneMoney) {
		this.oneMoney = oneMoney;
	}

	public Double getDayMoney() {
		return dayMoney;
	}

	public void setDayMoney(Double dayMoney) {
		this.dayMoney = dayMoney;
	}

	public Double getMonthMoney() {
		return monthMoney;
	}

	public void setMonthMoney(Double monthMoney) {
		this.monthMoney = monthMoney;
	}

	public Integer getOnlineBanking() {
		return onlineBanking;
	}

	public void setOnlineBanking(Integer onlineBanking) {
		this.onlineBanking = onlineBanking;
	}

	public Integer getQuickRecharge() {
		return quickRecharge;
	}

	public void setQuickRecharge(Integer quickRecharge) {
		this.quickRecharge = quickRecharge;
	}

	public Integer getTiecard() {
		return tiecard;
	}

	public void setTiecard(Integer tiecard) {
		this.tiecard = tiecard;
	}

	public Integer getUrgentWithdrawals() {
		return urgentWithdrawals;
	}

	public void setUrgentWithdrawals(Integer urgentWithdrawals) {
		this.urgentWithdrawals = urgentWithdrawals;
	}

	public Integer getSort() {
		return sort;
	}

	public void setSort(Integer sort) {
		this.sort = sort;
	}

	public Integer getIsDel() {
		return isDel;
	}

	public void setIsDel(Integer isDel) {
		this.isDel = isDel;
	}

	public PaymentChannel getChannel() {
		return channel;
	}

	public void setChannel(PaymentChannel channel) {
		this.channel = channel;
	}

	public PaymentBankInfo getBankInfo() {
		return bankInfo;
	}

	public void setBankInfo(PaymentBankInfo bankInfo) {
		this.bankInfo = bankInfo;
	}

	@Override
	public String toString() {
		return "PaymentBankChannel [id=" + id + ", bankId=" + bankId
				+ ", channelId=" + channelId + ", oneMoney=" + oneMoney
				+ ", dayMoney=" + dayMoney + ", monthMoney=" + monthMoney
				+ ", onlineBanking=" + onlineBanking + ", quickRecharge="
				+ quickRecharge + ", tiecard=" + tiecard
				+ ", urgentWithdrawals=" + urgentWithdrawals + ", sort=" + sort
				+ ", isDel=" + isDel + ", channel=" + channel + ", bankInfo="
				+ bankInfo + "]";
	}
	
	

}
