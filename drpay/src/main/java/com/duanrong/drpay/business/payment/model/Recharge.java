package com.duanrong.drpay.business.payment.model;

import java.util.Date;

/**
 * @Description: 充值
 * @Author: 林志明
 * @CreateDate: Sep 11, 2014
 */
public class Recharge {

	private String id;
	private String userId;
	private Date time;
	// 到账金额
	private Double actualMoney;
	private Date successTime;
	private String status;
	// 是否为管理员充值(0,1)
	private String isRechargedByAdmin;
	private Double fee;
	// 充值方式 android,PC,IOS
	private String rechargeWay;
	//充值类型 快捷，普通
	private String type;
	//第三方ID
	private String paymentId;
	
	private String cardNo;
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

	public Double getActualMoney() {
		return actualMoney;
	}

	public void setActualMoney(Double actualMoney) {
		this.actualMoney = actualMoney;
	}

	public Date getSuccessTime() {
		return successTime;
	}

	public void setSuccessTime(Date successTime) {
		this.successTime = successTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getIsRechargedByAdmin() {
		return isRechargedByAdmin;
	}

	public void setIsRechargedByAdmin(String isRechargedByAdmin) {
		this.isRechargedByAdmin = isRechargedByAdmin;
	}

	public Double getFee() {
		return fee;
	}

	public void setFee(Double fee) {
		this.fee = fee;
	}

	public String getRechargeWay() {
		return rechargeWay;
	}

	public void setRechargeWay(String rechargeWay) {
		this.rechargeWay = rechargeWay;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	
	public String getPaymentId() {
		return paymentId;
	}

	public void setPaymentId(String paymentId) {
		this.paymentId = paymentId;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	@Override
	public String toString() {
		return "Recharge [id=" + id + ", userId=" + userId + ", time=" + time
				+ ", actualMoney=" + actualMoney + ", successTime="
				+ successTime + ", status=" + status + ", isRechargedByAdmin="
				+ isRechargedByAdmin + ", fee=" + fee + ", rechargeWay="
				+ rechargeWay + ", type=" + type + "]";
	}

	

}
