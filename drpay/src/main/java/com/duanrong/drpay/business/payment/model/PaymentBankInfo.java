package com.duanrong.drpay.business.payment.model;

/**
 * 银行卡信息
 * 
 */
public class PaymentBankInfo {

	private String id;
	// 银行名称
	private String name;
	// 银行简称
	private String code;
	// 银行logo
	private String logo;
	// 富友银行机构码
	private String fuIouCode;
	
	/**** 显示需要，不与数据库映射 *****/
	//卡号
	private String cardNo;
	
	//卡片类型
	private String cardType;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public String getFuIouCode() {
		return fuIouCode;
	}

	public void setFuIouCode(String fuIouCode) {
		this.fuIouCode = fuIouCode;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getCardType() {
		return cardType;
	}

	public void setCardType(String cardType) {
		this.cardType = cardType;
	}

	@Override
	public String toString() {
		return "PaymentBankInfo [id=" + id + ", name=" + name + ", code="
				+ code + ", logo=" + logo + ", fuIouCode=" + fuIouCode
				+ ", cardNo=" + cardNo + ", cardType=" + cardType + "]";
	}


}
