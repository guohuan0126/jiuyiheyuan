package com.duanrong.business.payment.model;

import java.util.Date;

import base.model.BaseModel;

/**
 * @Description 用户通道令牌
 * @author JD
 * @CreateDate 2016年4月26日15:11:13
 */
public class PaymentUserRelation extends BaseModel{

	private static final long serialVersionUID = -4764570220301626167L;
	
	private String id;
	//用户ID
	private String userId;
	//用户令牌
	private String token;
	//通道ID
	private String paymentId;
	//是否禁用
	private String isAvailable;
	//创建时间
	private Date createTime;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getPaymentId() {
		return paymentId;
	}
	public void setPaymentId(String paymentId) {
		this.paymentId = paymentId;
	}
	public String getIsAvailable() {
		return isAvailable;
	}
	public void setIsAvailable(String isAvailable) {
		this.isAvailable = isAvailable;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	@Override
	public String toString() {
		return "PaymentUserRelation [id=" + id + ", userId=" + userId
				+ ", token=" + token + ", paymentId=" + paymentId
				+ ", isAvailable=" + isAvailable + ", createTime=" + createTime
				+ "]";
	}
	
}
