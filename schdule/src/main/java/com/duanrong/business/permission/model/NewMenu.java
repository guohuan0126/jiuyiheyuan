package com.duanrong.business.permission.model;

import base.model.BaseModel;

/**
 * @author xiao
 * @date 2015年1月29日 上午10:01:11
 */
public class NewMenu extends BaseModel {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8070184556982741436L;
	
	
	private int id; //菜单编号
	
	private String menuName; //菜单名称
	
	private int parentId; //父节点id
	
	private String url; //资源
	
	private String type; //菜单类型（保留字段）

	private int order; //排序字段
	
	private int status; //菜单状态

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public int getParentId() {
		return parentId;
	}

	public void setParentId(int parentId) {
		this.parentId = parentId;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getOrder() {
		return order;
	}

	public void setOrder(int order) {
		this.order = order;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
	

}
