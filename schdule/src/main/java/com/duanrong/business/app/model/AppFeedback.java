package com.duanrong.business.app.model;

import java.util.Date;

import base.model.BaseModel;

/**
 * APP意见反馈
 * 
 * @author Lin Zhiming
 * @version Jan 14, 2015 1:54:51 PM
 */
public class AppFeedback extends BaseModel {
	private static final long serialVersionUID = -1222045089476791282L;

	private Integer id;
	
	// 操作系统名称
	private String osName;
	
	// 反馈内容
	private String content;
	
	// 添加时间
	private Date time;
	
	// 查询开始时间(条件查询用)
	private String startTime;

	// 查询结束时间(条件查询用)
	private String endTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOsName() {
		return osName;
	}

	public void setOsName(String osName) {
		this.osName = osName;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	@Override
	public String toString() {
		return "APPFeedback [id=" + id + ", osName=" + osName + ", content="
				+ content + ", time=" + time + "]";
	}

}