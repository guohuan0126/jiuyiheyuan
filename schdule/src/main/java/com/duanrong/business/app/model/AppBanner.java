package com.duanrong.business.app.model;

import java.util.Date;

/**
 * @Description: 手机APPBanner图
 * @Author: 邱飞虎
 * @CreateDate: 2015年6月5日10:30:02
 */
public class AppBanner {
    
	private String picID;         //编号
	private String picName;       //图片标题
	private String picPosition;   //图片位置
	private String picUrl;        //页面地址
	private String seqNum;        //排序
	private String picStatus;     //状态：0.禁用  1.启用
	private String description;   //描述
	private Date time;            //添加时间
	private String startTime;    //开始时间(查询用)
	private String endTime;      //结束时间(查询用)
	
	public String getPicID() {
		return picID;
	}
	public void setPicID(String picID) {
		this.picID = picID;
	}
	public String getPicName() {
		return picName;
	}
	public void setPicName(String picName) {
		this.picName = picName;
	}
	public String getPicPosition() {
		return picPosition;
	}
	public void setPicPosition(String picPosition) {
		this.picPosition = picPosition;
	}
	public String getPicUrl() {
		return picUrl;
	}
	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}
	public String getSeqNum() {
		return seqNum;
	}
	public void setSeqNum(String seqNum) {
		this.seqNum = seqNum;
	}
	public String getPicStatus() {
		return picStatus;
	}
	public void setPicStatus(String picStatus) {
		this.picStatus = picStatus;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	
	
	
}
