package com.duanrong.business.sms.model;

import java.util.Date;

import base.model.BaseModel;

import com.duanrong.business.user.model.User;

/**
 * @Description: 短信
 * @Author: 林志明
 * @CreateDate: Nov 24, 2014
 */
public class Sms extends BaseModel {

	private static final long serialVersionUID = 4028643806162628481L;

	private String id;
	private String userId;
	private String mobileNumber;
	private String content;
	private Date time;
	private String type;
	private User user;
	private int num;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getId() {
		return id;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	@Override
	public String toString() {
		return "Sms [id=" + id + ", userId=" + userId + ", mobileNumber="
				+ mobileNumber + ", content=" + content + ", time=" + time
				+ ", type=" + type + "]";
	}

}