package base.exception;

/**
 * 可用余额不足
 * 
 * @author Lin Zhiming
 * @version May 27, 2015 3:08:35 PM
 */
public class InsufficientBalanceException extends Exception {

	public InsufficientBalanceException() {
		super();
	}

	public InsufficientBalanceException(String msg, Throwable e) {
		super(msg, e);
	}

	public InsufficientBalanceException(String msg) {
		super(msg);
	}

	private static final long serialVersionUID = -3025535967802089871L;
}
