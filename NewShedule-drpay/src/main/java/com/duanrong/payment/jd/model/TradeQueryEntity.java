package com.duanrong.payment.jd.model;

import java.io.Serializable;


public class TradeQueryEntity implements Serializable {

    /**
	 * 
	 */
	private static final long serialVersionUID = -1626380259485072656L;
	private String version="";
    private String merchantNum="";
    private  String merchantSign="";
    private String data="";

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getMerchantNum() {
        return merchantNum;
    }

    public void setMerchantNum(String merchantNum) {
        this.merchantNum = merchantNum;
    }

    public String getMerchantSign() {
        return merchantSign;
    }

    public void setMerchantSign(String merchantSign) {
        this.merchantSign = merchantSign;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
