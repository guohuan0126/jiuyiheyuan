package com.duanrong.business.link.model;

import base.model.BaseModel;

/**
 * @Description: 友情链接、媒体报道等链接
 * @Author: 林志明
 * @CreateDate: Dec 1, 2014
 */
public class Link extends BaseModel {
	private static final long serialVersionUID = -5339830976712847846L;

	private String id;
	private String name;
	private String type;
	private String url;
	private String logo;
	private String description;
	private String masterEmail;
	private String position;
	private String status;
	private Integer seqNum;
	private LinkType linkType;

	
	
	public LinkType getLinkType() {
		return linkType;
	}

	public void setLinkType(LinkType linkType) {
		this.linkType = linkType;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getMasterEmail() {
		return masterEmail;
	}

	public void setMasterEmail(String masterEmail) {
		this.masterEmail = masterEmail;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Integer getSeqNum() {
		return seqNum;
	}

	public void setSeqNum(Integer seqNum) {
		this.seqNum = seqNum;
	}

	
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	@Override
	public String toString() {
		return "Link [id=" + id + ", name=" + name + ", url=" + url + ", logo="
				+ logo + ", description=" + description + ", masterEmail="
				+ masterEmail + ", position=" + position + ", status=" + status
				+ ", seqNum=" + seqNum + "]";
	}

}