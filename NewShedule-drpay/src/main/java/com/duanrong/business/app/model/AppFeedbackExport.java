package com.duanrong.business.app.model;

import java.util.Date;

import base.model.BaseModel;

/**
 * APP意见反馈用于Excel导出
 * 
 * @author Qiu Feihu
 * @version 2015年6月4日16:42:41
 */
public class AppFeedbackExport extends BaseModel {

	private static final long serialVersionUID = 7703566901836403026L;

	//编号
	private Integer id;
	
	// 操作系统名称
	private String osName;
	
	// 反馈内容
	private String content;
	
	// 添加时间
	private String time;
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOsName() {
		return osName;
	}

	public void setOsName(String osName) {
		this.osName = osName;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	@Override
	public String toString() {
		return "APPFeedback [id=" + id + ", osName=" + osName + ", content="
				+ content + ", time=" + time + "]";
	}

}