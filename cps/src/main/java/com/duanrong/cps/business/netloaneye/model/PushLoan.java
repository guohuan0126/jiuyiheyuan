package com.duanrong.cps.business.netloaneye.model;

import java.util.Date;

public class PushLoan {
    private String id;

    private String loanId;

    private Date pushTime;

    private String pushUserid;
    
    private String status;
    
    private Integer pushStatus;
    
    private Double loanMoney;
    
    private Double alreadyMoney;
    
    private Date updateTime;
    
    private String message;
    
    private String type;
    
    
    

    public Integer getPushStatus() {
		return pushStatus;
	}

	public void setPushStatus(Integer pushStatus) {
		this.pushStatus = pushStatus;
	}

	public Double getLoanMoney() {
		return loanMoney;
	}

	public void setLoanMoney(Double loanMoney) {
		this.loanMoney = loanMoney;
	}

	public Double getAlreadyMoney() {
		return alreadyMoney;
	}

	public void setAlreadyMoney(Double alreadyMoney) {
		this.alreadyMoney = alreadyMoney;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLoanId() {
        return loanId;
    }

    public void setLoanId(String loanId) {
        this.loanId = loanId;
    }

    public Date getPushTime() {
        return pushTime;
    }

    public void setPushTime(Date pushTime) {
        this.pushTime = pushTime;
    }

    public String getPushUserid() {
        return pushUserid;
    }

    public void setPushUserid(String pushUserid) {
        this.pushUserid = pushUserid;
    }
}