package com.duanrong.cps.business.bsy.model;

import java.util.Date;

public class PlatformUserRelation {

	/**
	 * UUID的主键
	 */
	private String id;
	/**
	 * 网贷天眼用户在网贷天眼的ID
	 */
	private String WDTY_id;
	/**
	 * 在我公司注册用户的ID
	 */
	private String userID;
	/**
	 * 是否为新用户
	 */
	private String whetherNew;
	/**
	 * 用来验证自动登录天眼绑定的标识
	 */
	private String userKey;
	/**
	 * 注册时间
	 */
	private Date createTime;
	/**
	 * 标记是否解除绑定(0,为解除绑定;1,为绑定,默认值1)
	 */
	private String whetherBinding;
	/**
	 * 值为是WDTY用户,值为BSY是比搜益推的用户
	 */
    private String type;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getWDTY_id() {
		return WDTY_id;
	}
	public void setWDTY_id(String wDTY_id) {
		WDTY_id = wDTY_id;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getWhetherNew() {
		return whetherNew;
	}
	public void setWhetherNew(String whetherNew) {
		this.whetherNew = whetherNew;
	}
	public String getUserKey() {
		return userKey;
	}
	public void setUserKey(String userKey) {
		this.userKey = userKey;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getWhetherBinding() {
		return whetherBinding;
	}
	public void setWhetherBinding(String whetherBinding) {
		this.whetherBinding = whetherBinding;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
    
	

    
}
