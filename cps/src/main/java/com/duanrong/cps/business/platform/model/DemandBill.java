package com.duanrong.cps.business.platform.model;

import java.util.Date;

public class DemandBill {
      private String id;
      private Double money;
      private String type;
      private Date  createTime;
      private String tranferWay;
      private String detail;
      private String info;
      private String billId;
      private String isRead;
      
      private String userId;
      private String realname;
      private String mobileNumber;
      private String userSource;
	
      
    
	public String getRealname() {
		return realname;
	}
	public void setRealname(String realname) {
		this.realname = realname;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getTranferWay() {
		return tranferWay;
	}
	public void setTranferWay(String tranferWay) {
		this.tranferWay = tranferWay;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getBillId() {
		return billId;
	}
	public void setBillId(String billId) {
		this.billId = billId;
	}
	public String getIsRead() {
		return isRead;
	}
	public void setIsRead(String isRead) {
		this.isRead = isRead;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getUserSource() {
		return userSource;
	}
	public void setUserSource(String userSource) {
		this.userSource = userSource;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	@Override
	public String toString() {
		return "DemandBill [id=" + id + ", money=" + money + ", type=" + type
				+ ", createTime=" + createTime + ", tranferWay=" + tranferWay
				+ ", detail=" + detail + ", info=" + info + ", billId="
				+ billId + ", isRead=" + isRead + ", userId=" + userId
				+ ", realname=" + realname + ", mobileNumber=" + mobileNumber
				+ ", userSource=" + userSource + "]";
	}
      
      
}
