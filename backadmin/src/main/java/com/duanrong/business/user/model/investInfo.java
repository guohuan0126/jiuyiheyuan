package com.duanrong.business.user.model;

import java.util.Date;

public class investInfo {
	
	private String investId;
	private String loanName;
	private String operationType;
	private String deadline;
	private String day;
	private double investMoney;
	private double rate;
	private Date investTime;
	private String isAutoInvest;
	private String investStauts;
	private String redpacketId;
	
	
	public String getRedpacketId() {
		return redpacketId;
	}
	public void setRedpacketId(String redpacketId) {
		this.redpacketId = redpacketId;
	}
	public String getOperationType() {
		return operationType;
	}
	public void setOperationType(String operationType) {
		this.operationType = operationType;
	}
	public String getDeadline() {
		return deadline;
	}
	public void setDeadline(String deadline) {
		this.deadline = deadline;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getInvestId() {
		return investId;
	}
	public void setInvestId(String investId) {
		this.investId = investId;
	}
	public String getLoanName() {
		return loanName;
	}
	public void setLoanName(String loanName) {
		this.loanName = loanName;
	}
	public double getInvestMoney() {
		return investMoney;
	}
	public void setInvestMoney(double investMoney) {
		this.investMoney = investMoney;
	}
	public double getRate() {
		return rate;
	}
	public void setRate(double rate) {
		this.rate = rate;
	}
	public Date getInvestTime() {
		return investTime;
	}
	public void setInvestTime(Date investTime) {
		this.investTime = investTime;
	}
	public String getIsAutoInvest() {
		return isAutoInvest;
	}
	public void setIsAutoInvest(String isAutoInvest) {
		this.isAutoInvest = isAutoInvest;
	}
	public String getInvestStauts() {
		return investStauts;
	}
	public void setInvestStauts(String investStauts) {
		this.investStauts = investStauts;
	}
	
}
