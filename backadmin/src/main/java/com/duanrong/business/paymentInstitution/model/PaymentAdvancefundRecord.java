package com.duanrong.business.paymentInstitution.model;

import java.util.Date;

/**
 * 垫付资金记录表
 * @author bj
 *
 */
public class PaymentAdvancefundRecord {
	/**
	 * 编号
	 */
	private String id;
	/**
	 * 创建时间
	 */
	private Date carateTime;
	/**
	 * 金额
	 */
	private double money;
	/**
	 * 类型 充值 提现
	 */
	private String type;
	/**
	 * 创建者
	 */
	private String creater;
	/**
	 * 支付通道
	 */
	private String paymentId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Date getCarateTime() {
		return carateTime;
	}
	public void setCarateTime(Date carateTime) {
		this.carateTime = carateTime;
	}
	public double getMoney() {
		return money;
	}
	public void setMoney(double money) {
		this.money = money;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCreater() {
		return creater;
	}
	public void setCreater(String creater) {
		this.creater = creater;
	}
	public String getPaymentId() {
		return paymentId;
	}
	public void setPaymentId(String paymentId) {
		this.paymentId = paymentId;
	}
	
	
}
