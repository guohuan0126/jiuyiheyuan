package com.duanrong.business.payMentChannel.model;

public class PayMentChannel {

	//id
	private String id;
	//渠道名称
	private String name;
	//渠道字母缩写
	private String code;
	//渠道logo
	private String logo;
	//普通充值费率
	private double rateGateway;
	//快捷充值费率
	private double rateQuick;
	//提现手续费
	private double withdrawFee;
	//是否可用
	private String usable;
	//是否移动端可用
	private String mobileUsable;
	
	
	public String getMobileUsable() {
		return mobileUsable;
	}
	public void setMobileUsable(String mobileUsable) {
		this.mobileUsable = mobileUsable;
	}
	public String getUsable() {
		return usable;
	}
	public void setUsable(String usable) {
		this.usable = usable;
	}
	// 排序
	private String sort;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public double getRateGateway() {
		return rateGateway;
	}
	public void setRateGateway(double rateGateway) {
		this.rateGateway = rateGateway;
	}
	public double getRateQuick() {
		return rateQuick;
	}
	public void setRateQuick(double rateQuick) {
		this.rateQuick = rateQuick;
	}
	public double getWithdrawFee() {
		return withdrawFee;
	}
	public void setWithdrawFee(double withdrawFee) {
		this.withdrawFee = withdrawFee;
	}
	
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	
	
}
